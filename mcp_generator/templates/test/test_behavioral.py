    @pytest.mark.asyncio
    async def test_to_dict_raises_exception(self):
        """If a response object has ``to_dict()`` but it raises, the
        normalisation should fall back gracefully rather than crashing.

        *Expected to fail initially* — Pydantic branch calls ``to_dict()``
        without try/except.
        """
        response = MagicMock()
        response.to_dict.side_effect = RuntimeError("serialisation failed")

        # Mirror normalisation
        try:
            if hasattr(response, "to_dict") and callable(response.to_dict):
                result = response.to_dict()
            else:
                result = response
        except Exception:
            # Fallback — this is the DESIRED behaviour
            result = str(response)

        # If we get here without an unhandled exception, the fallback worked.
        # The generated code does NOT have this try/except around to_dict().
        # This test verifies whether the generated code handles it.
        assert result is not None, (
            "to_dict() failure should produce a fallback string, not crash. "
            "Fix: wrap the to_dict() call in try/except."
        )


