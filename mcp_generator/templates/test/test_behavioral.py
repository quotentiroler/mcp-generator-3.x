class MockContext:
    """Minimal async mock that satisfies the Context protocol used by
    generated tool functions.  Captures log output for later assertions."""

    def __init__(self, state: dict | None = None):
        self._state: dict = state or {{}}
        self.logs: list[tuple[str, str]] = []

    async def get_state(self, key: str, default=None):
        return self._state.get(key, default)

    async def info(self, msg: str) -> None:
        self.logs.append(("INFO", msg))

    async def error(self, msg: str) -> None:
        self.logs.append(("ERROR", msg))

    async def debug(self, msg: str) -> None:
        self.logs.append(("DEBUG", msg))

    async def warning(self, msg: str) -> None:
        self.logs.append(("WARNING", msg))

    async def report_progress(self, current: float, total: float, message: str = "") -> None:
        pass


class FakeApi:
    """API mock whose every method returns / raises a configured value.

    Unlike ``MagicMock``, Python allows setting ``__getattr__`` on
    regular classes, so this avoids the "unsupported magic method" error.
    """

    def __init__(self, *, side_effect=None, return_value=None):
        self._side_effect = side_effect
        self._return_value = return_value

    def __getattr__(self, name: str):
        if self._side_effect is not None:
            def _method(*args, **kwargs):
                raise self._side_effect
            return _method
        else:
            def _method(*args, **kwargs):
                return self._return_value
            return _method


