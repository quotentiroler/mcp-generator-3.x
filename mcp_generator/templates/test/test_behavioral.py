def _discover_tools() -> list[tuple[str, str, Any, FastMCP]]:
    """Return ``(module_name, tool_name, tool_fn, mcp_instance)`` for
    every tool registered across all server modules.

    Discovery happens once at import time so ``pytest.mark.parametrize``
    can reference the list.
    """
    results: list[tuple[str, str, Any, FastMCP]] = []

    async def _collect():
        for mod_name, mcp_inst in MODULE_INSTANCES:
            tool_list = await mcp_inst.list_tools()
            for tool_mcp in tool_list:
                tool_obj = await mcp_inst.get_tool(tool_mcp.name)
                results.append((mod_name, tool_mcp.name, tool_obj.fn, mcp_inst))

    asyncio.run(_collect())
    return results


ALL_TOOLS = _discover_tools()
ALL_TOOL_IDS = [f"{{mod}}/{{name}}" for mod, name, _, _ in ALL_TOOLS]


def _discover_pydantic_tools() -> list[tuple[str, str, Any, FastMCP]]:
    """Subset of ALL_TOOLS whose function body contains ``json.loads`` —
    i.e. tools that accept a JSON-encoded Pydantic model parameter."""
    results = []
    for mod, name, fn, mcp_inst in ALL_TOOLS:
        try:
            src = inspect.getsource(fn)
        except (OSError, TypeError):
            continue
        if "json.loads" in src:
            results.append((mod, name, fn, mcp_inst))
    return results


PYDANTIC_TOOLS = _discover_pydantic_tools()
PYDANTIC_TOOL_IDS = [f"{{mod}}/{{name}}" for mod, name, _, _ in PYDANTIC_TOOLS]

NON_PYDANTIC_TOOLS = [t for t in ALL_TOOLS if t not in PYDANTIC_TOOLS]
NON_PYDANTIC_TOOL_IDS = [f"{{mod}}/{{name}}" for mod, name, _, _ in NON_PYDANTIC_TOOLS]


def _minimal_args(fn) -> dict[str, str]:
    """Build minimal keyword arguments for *fn* — every parameter
    (except ``ctx``) gets an empty-string value."""
    sig = inspect.signature(fn)
    return {{
        p.name: ""
        for p in sig.parameters.values()
        if p.name != "ctx"
    }}


def _make_api_dict(fake_api):
    """Return a defaultdict that maps every key to *fake_api*.

    ``_get_api_instances`` returns a plain dict like
    ``{{'pet_api': PetApi(client)}}``.  We replace it with a defaultdict
    so whatever key the tool looks up, it gets our fake."""
    return defaultdict(lambda: fake_api)


